<h1>Keyboard shortcuts</h1>

<p>
    The code and content editors on this site are meant to be used efficiently with keyboard shortcuts.
    The following keyboard shortcuts may not all be available in all editors, as some are context-dependent and only work for certain types of input.
</p>

<h2>Toolbar shortcuts</h2>

<p>
    When a toolbar is displayed, you can generally activate the buttons on it using keyboard shortcuts.
    Toolbar buttons should show you their keyboard shortcut when you hover over them with your mouse.
</p>

<h3>Headers</h3>

<p>
    Headers have an additional keyboard shortcut, on top of the <kbd>Ctrl-H</kbd> shortcut that cycles through header levels starting at 1 and counting up.
    <kbd>Ctrl-Shift-H</kdb> cycles through headers in the <em>other</em> direction.
</p>

<h2>Indentation depth</h2>

<p>
    Most content that is aware of its indentation levels, such as Markdown lists, can have its indentation level adjusted in bulk.
    If you select multiple lines, you can press <kbd>Tab</kdb> or <kbd>Shift-Tab</kbd> to increase or decrease the indentation level of all the selected lines, respectively.
</p>

<h2>Search/replace</h2>

<p>
    Editors have a built-in tool for doing search/replace operations on your content.
    The search and replace interface is highly keyboard-shortcut-centric.
</p>

<ul>
    <li><kbd>Ctrl-F</kbd> Open search interface. Press <kbd>Enter</kbd> to apply your search, and highlight all instances of your input.</li>
    <li><kbd>Ctrl-G</kbd> Cycle through search results.</li>
    <li><kbd>Ctrl-H</kbd> Open the search/replace interface.</li>
</ul>